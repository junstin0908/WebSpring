package j07_반복;

public class For1 {
	
	public static void main(String[] args) {
		System.out.println("for 전");
		for(int i = 0; i < 5; i++) {
			System.out.println(i);
		}
		System.out.println("for 후");
		
		/*
		 * <for 문의 기본 형태>
		 * for(초기치; 조건문; 증가치) {
		 * 		실행할 문장 a ;
		 * 		실행할 문장 b ;
		 * 		.......
		 * }
		 */
	}

}
