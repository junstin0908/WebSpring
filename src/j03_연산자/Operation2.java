package j03_연산자;

public class Operation2 {
	public static void main(String[] args) {
		
		// 증감 연산자
		
		int num = 50;
		System.out.println(num);
		num++;
		System.out.println(num);
		++num;
		System.out.println(num);
		System.out.println();
		
		int num2 = 50;
		
		System.out.println(num2);
		System.out.println(++num2);
		System.out.println(num2);
		System.out.println(num2++);
		System.out.println(num2);

		System.out.println();

		num2 = 50;
		
		System.out.println(num2);
		System.out.println(--num2);
		System.out.println(num2);
		System.out.println(num2--);
		System.out.println(num2);
	}

}
